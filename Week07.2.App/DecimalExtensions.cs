namespace Week07._2.App
{
    public static class DecimalExtensions
    {
        public static decimal Aduna(this decimal a, decimal b)
        {
            return a + b;
        }

        public static decimal Scade(this decimal a, decimal b)
        {
            return a - b;
        }

        public static decimal Inmulteste(this decimal a, decimal b)
        {
            return a * b;
        }
    }
}
